                field_name4.SetWidth(10)
                field_name5.SetWidth(10)
                
                layer.CreateField(field_name1)
                layer.CreateField(field_name2)
                layer.CreateField(field_name3)
                layer.CreateField(field_name4)
                layer.CreateField(field_name5)
